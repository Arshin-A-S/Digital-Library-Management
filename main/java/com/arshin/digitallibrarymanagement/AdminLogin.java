    public AdminLogin(WelcomeMenu wm) {
        initComponents();
        databaseConnect();

        loginButton.addActionListener(new ActionListener(){
            @Override
            public void actionPerformed(ActionEvent e){
                try {
                    String username = usernameField.getText().strip();
                    String pass = new String(passwordField.getPassword()).strip();
                    int adminId;
                    String checkPass;
                    String getPassQuery = "select password,admin_id from admins where admin_name=?;";
                    PreparedStatement passQueryStmt= con.prepareStatement(getPassQuery);
                    passQueryStmt.setString(1, username);
                    ResultSet rs = passQueryStmt.executeQuery();
                    if(!rs.next()){
                        JOptionPane.showMessageDialog(AdminLogin.this, "Error: Enter the correct admin_name", "Unknown Username", JOptionPane.ERROR_MESSAGE);
                    }else{
                        checkPass = rs.getString("password");
                        adminId = rs.getInt("admin_id");
                        if (checkPass.equals(pass)) {
                            dispose();
                            setVisible(false); //TEST THIS FEATURE
                            new AdminMenu(AdminLogin.this,adminId);
                        }else{
                            JOptionPane.showMessageDialog(AdminLogin.this, "Error: Enter the correct password", "Incorrect Password", JOptionPane.ERROR_MESSAGE);
                        }
                    }
                }catch(SQLException sqlE){
                    sqlE.printStackTrace();
                    JOptionPane.showMessageDialog(AdminLogin.this, "Error "+sqlE.getMessage(), "Database Query Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        });

        cancelButton.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                dispose();
                wm.setVisible(true);
            }
        });

        this.setContentPane(this.adminLoginPanel);
        this.setVisible(true);
        this.setSize(1000,750);
        this.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
    }

    private void databaseConnect(){
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/DigitalLibrary", "root", "1234");
        } catch(Exception SqlConnE){
            SqlConnE.printStackTrace();
            JOptionPane.showMessageDialog(null, "Database Connection Error: "+SqlConnE.getMessage(), "Database Connection Error", JOptionPane.ERROR_MESSAGE);
        }
    }

